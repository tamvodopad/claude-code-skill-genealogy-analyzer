def normalize_name(name: str) -> str:
    """–ù–æ—Ä–º–∞–ª–∏–∑–∞—Ü–∏—è –∏–º–µ–Ω–∏ –¥–ª—è —Å—Ä–∞–≤–Ω–µ–Ω–∏—è."""
    name = name.strip()
    # –£–±–∏—Ä–∞–µ–º —Å–ø–µ—Ü—Å–∏–º–≤–æ–ª—ã (?, !, —Å–∫–æ–±–∫–∏ –∏ —Ç.–¥.)
    name = re.sub(r'[?\!\(\)\[\]]', '', name).strip()
    # –ü–µ—Ä–≤–∞—è –±—É–∫–≤–∞ –∑–∞–≥–ª–∞–≤–Ω–∞—è
    if name:
        name = name[0].upper() + name[1:].lower() if len(name) > 1 else name.upper()
    # –ó–∞–º–µ–Ω–∞ –µ –Ω–∞ —ë –≤ –∏–∑–≤–µ—Å—Ç–Ω—ã—Ö –∏–º–µ–Ω–∞—Ö
    name_with_yo = {
        "–ü–µ—Ç—Ä": "–ü—ë—Ç—Ä",
        "–§–µ–¥–æ—Ä": "–§—ë–¥–æ—Ä",
        "–°–µ–º–µ–Ω": "–°–µ–º—ë–Ω",
        "–ê—Ä—Ç–µ–º": "–ê—Ä—Ç—ë–º",
        "–ú–∞—Ç—Ä–µ–Ω–∞": "–ú–∞—Ç—Ä—ë–Ω–∞",
        "–ê–ª–µ–Ω–∞": "–ê–ª—ë–Ω–∞",
    }
    name = name_with_yo.get(name, name)
    return NAME_VARIANTS.get(name, name)


def parse_gedcom_date(date_str: str) -> Tuple[Optional[date], bool, str]:
    """–ü–∞—Ä—Å–∏–Ω–≥ –¥–∞—Ç—ã –∏–∑ GEDCOM —Ñ–æ—Ä–º–∞—Ç–∞."""
    if not date_str:
        return None, False, ""

    date_str = date_str.strip()
    is_julian = "@#DJULIAN@" in date_str
    clean_str = date_str.replace("@#DJULIAN@", "").strip()

    # –£–±–∏—Ä–∞–µ–º –º–æ–¥–∏—Ñ–∏–∫–∞—Ç–æ—Ä—ã
    for prefix in ["ABT", "BEF", "AFT", "EST", "CAL"]:
        clean_str = clean_str.replace(prefix, "").strip()

    months = {
        'JAN': 1, 'FEB': 2, 'MAR': 3, 'APR': 4, 'MAY': 5, 'JUN': 6,
        'JUL': 7, 'AUG': 8, 'SEP': 9, 'OCT': 10, 'NOV': 11, 'DEC': 12
    }

    # –ü–æ–ª–Ω–∞—è –¥–∞—Ç–∞: "15 MAY 1893"
    match = re.match(r'(\d{1,2})\s+([A-Z]{3})\s+(\d{4})', clean_str)
    if match:
        day, month_str, year = match.groups()
        if month_str in months:
            try:
                return date(int(year), months[month_str], int(day)), is_julian, date_str
            except ValueError:
                pass

    return None, is_julian, date_str


def get_saints_for_date(d: date, window: int = 0) -> Tuple[Set[str], Set[str]]:
    """
    –ü–æ–ª—É—á–µ–Ω–∏–µ –∏–º—ë–Ω —Å–≤—è—Ç—ã—Ö –¥–ª—è –¥–∞—Ç—ã (–∏ –æ–∫–Ω–∞ –≤–æ–∫—Ä—É–≥ –Ω–µ—ë).
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç (–º—É–∂—Å–∫–∏–µ –∏–º–µ–Ω–∞, –∂–µ–Ω—Å–∫–∏–µ –∏–º–µ–Ω–∞).
    """
    male_names = set()
    female_names = set()

    for offset in range(-window, window + 1):
        check_date = d + timedelta(days=offset)
        key = (check_date.month, check_date.day)
        if key in SAINTS_CALENDAR:
            m, f = SAINTS_CALENDAR[key]
            male_names.update(m)
            female_names.update(f)

    return male_names, female_names


def parse_gedcom(filepath: str) -> List[Person]:
    """–ü–∞—Ä—Å–∏–Ω–≥ GEDCOM —Ñ–∞–π–ª–∞ –¥–ª—è –∏–∑–≤–ª–µ—á–µ–Ω–∏—è –ø–µ—Ä—Å–æ–Ω —Å –¥–∞—Ç–∞–º–∏ –∫—Ä–µ—â–µ–Ω–∏—è."""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    lines = content.split('\n')
    persons = []

    current_id = None
    current_name = ""
    current_given = ""
    current_sex = ""
    current_birth_date = None
    current_birth_raw = ""
    current_chr_date = None
    current_chr_raw = ""
    current_is_julian = False
    in_birth = False
    in_chr = False

    for line in lines:
        line = line.strip()
        if not line:
            continue

        # –ù–æ–≤–∞—è –ø–µ—Ä—Å–æ–Ω–∞
        if line.startswith("0 @I"):
            # –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø—Ä–µ–¥—ã–¥—É—â—É—é
            if current_id and current_chr_date:
                persons.append(Person(
                    id=current_id,
                    name=current_name,
                    given_name=current_given,
                    sex=current_sex,
                    birth_date=current_birth_date,
                    birth_date_raw=current_birth_raw,
                    christening_date=current_chr_date,
                    christening_date_raw=current_chr_raw,
                    is_julian=current_is_julian
                ))

            # –ù–æ–≤–∞—è –∑–∞–ø–∏—Å—å
            match = re.match(r'0 (@I\d+@)', line)
            if match:
                current_id = match.group(1)
                current_name = ""
                current_given = ""
                current_sex = ""
                current_birth_date = None
                current_birth_raw = ""
                current_chr_date = None
                current_chr_raw = ""
                current_is_julian = False
                in_birth = False
                in_chr = False
            continue

        if not current_id:
            continue

        # –£—Ä–æ–≤–µ–Ω—å 1
        if line.startswith("1 "):
            in_birth = False
            in_chr = False

            if line.startswith("1 NAME "):
                current_name = line[7:].replace("/", "").strip()
            elif line.startswith("1 SEX "):
                current_sex = line[6:].strip()
            elif line.startswith("1 BIRT"):
                in_birth = True
            elif line.startswith("1 CHR"):
                in_chr = True

        # –£—Ä–æ–≤–µ–Ω—å 2
        elif line.startswith("2 "):
            if line.startswith("2 GIVN "):
                # –ë–µ—Ä—ë–º —Ç–æ–ª—å–∫–æ –ø–µ—Ä–≤–æ–µ –∏–º—è
                given = line[7:].strip().split()[0] if line[7:].strip() else ""
                current_given = given
            elif line.startswith("2 DATE "):
                date_str = line[7:].strip()
                parsed, is_julian, raw = parse_gedcom_date(date_str)
                if in_birth:
                    current_birth_date = parsed
                    current_birth_raw = raw
                elif in_chr:
                    current_chr_date = parsed
                    current_chr_raw = raw
                    current_is_julian = is_julian

    # –ü–æ—Å–ª–µ–¥–Ω—è—è –ø–µ—Ä—Å–æ–Ω–∞
    if current_id and current_chr_date:
        persons.append(Person(
            id=current_id,
            name=current_name,
            given_name=current_given,
            sex=current_sex,
            birth_date=current_birth_date,
            birth_date_raw=current_birth_raw,
            christening_date=current_chr_date,
            christening_date_raw=current_chr_raw,
            is_julian=current_is_julian
        ))

    return persons


